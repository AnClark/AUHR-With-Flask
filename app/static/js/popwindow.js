        function show_popup_window(){
            document.getElementById("shader").style.visibility="visible";
            document.getElementById("popup-window").style.visibility="visible";
        }
        function hide_popup_window(){
            document.getElementById("popup-window").style.visibility="hidden";
            document.getElementById("shader").style.visibility="hidden";
        }